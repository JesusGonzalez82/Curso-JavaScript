// Operadores de Asignacion

let num = 10;

num += 5;

document.write(num);

// Operadores Aritmeticos
/* Los operadores aritmeticos toman valores numericos (ya sean literales o variables) como sus operandos y retornan un valor numerico unico.*/

num--;

alert(num);

/* Operadores de Comparacion
Los operadores de comparacion comparan dos expresiones y devuelven un boolean que representa la relación entre sus valores
*/

let numero = 23;
let numero1 = 13;

document.write(numero == numero1);

/* Operadores Logicos
Los operadores logicos nos devuelven un resultado a partir de que se cumpla (o no) una condición, su resultado es booleano, y sus operandos son valores logicos o asimilabes entre ellos

let valor = true;
let valor2 = true;

let resultado = valor && valor2 --> Si todas las condiciones se cumplen es TRUE

let resultado = valor || valor2  --> Con que una de las condiciones se cumplan es TRUE

let resultado = valor != valor2 --> Nos devuelve lo contrario de lo que devolvemos
 */








